void	ft_putchar(char c);

void	rush(int x, int y)
{
	int		l;
	int		h;

	h = 0;
	while (h++ < y)
	{
		l = 0;
		while (l++ < x)
		{
			if (h == 1 || h == y)
			{
				if (l == 1)
					ft_putchar((l == 1 || l == x) ? ('A') : ('B'));
				else
					ft_putchar((l == 1 || l == x) ? ('C') : ('B'));
			}
			else
				ft_putchar((l == 1 || l == x) ? ('B') : (' '));
		}
		ft_putchar('\n');
	}
}
