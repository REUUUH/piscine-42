char	*ft_strcat(char *dest, char *src)
{
	int i;
	int v;

	v = 0;
	i = 0;
	while (dest[i])
		i++;
	while (src[v])
	{
		dest[i] = src[v];
		i++;
		v++;
	}
	dest[i] = '\0';
	return (dest);
}
